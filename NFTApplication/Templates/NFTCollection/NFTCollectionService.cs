        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Web3 web3, NFTCollectionDeployment nFTCollectionDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<NFTCollectionDeployment>().SendRequestAndWaitForReceiptAsync(nFTCollectionDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Web3 web3, NFTCollectionDeployment nFTCollectionDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<NFTCollectionDeployment>().SendRequestAsync(nFTCollectionDeployment);
        }

        public static async Task<NFTCollectionService> DeployContractAndGetServiceAsync(Web3 web3, NFTCollectionDeployment nFTCollectionDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, nFTCollectionDeployment, cancellationTokenSource);
            return new NFTCollectionService(web3, receipt.ContractAddress);
        }

